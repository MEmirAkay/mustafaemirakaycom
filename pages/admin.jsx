import Head from "next/head";
import { React} from "react";
import dynamic from "next/dynamic";
import ArticleList from "../components/articlelist";
import { server } from "../config";

const CKEditorApp = dynamic(() => import("../components/ckeditor"), {
  ssr: false,
});

export default function Admin({data}) {
  return (
    <div className="w-full h-full">
      <Head>
        <title>MEA</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="mx-auto w-7/12 py-10">
        <table className="table-auto border-2">
          <thead>
            <tr>
              <th className="border-2">Başlık</th>
              <th className="border-2">Yayın Tarihi</th>
              <th className="border-2">İşlem</th>
            </tr>
          </thead>
          <tbody>
            <ArticleList data={data} />
          </tbody>
        </table>
      </div>

      <CKEditorApp />
    </div>
  );
}

export async function getServerSideProps() {
  const res = await fetch(`${server}/api/admin/article`, {
    method: "GET",
  });
  try {
    const data = await res.json();

    return {
      props: {
        data,
      },
    };
  } catch (err) {
    console.log("Error: ", err.message);
    return err.message;
  }
}
